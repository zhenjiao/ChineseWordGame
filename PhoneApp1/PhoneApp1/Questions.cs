        public CombinedQuestion GetRandomCombinedQuestion()
        {
            var question = new CombinedQuestion();
            Random rand = new Random();
            int qIndex1 = rand.Next(Questions.Count);
            while (m_usedQuestions.Contains(qIndex1))
            {
                qIndex1 = (qIndex1 + 1) % Questions.Count;
            }
            m_usedQuestions.Add(qIndex1);
            int qIndex2 = rand.Next(Questions.Count);
            while (m_usedQuestions.Contains(qIndex2))
            {
                qIndex2 = (qIndex2 + 1) % Questions.Count;
            }
            m_usedQuestions.Add(qIndex2);
            int aIndex = rand.Next(4);
            
            var q1 = Questions[qIndex1];
            var q2 = Questions[qIndex2];
            question.Text = q1.Text + ", " + q2.Text;
            question.Answers = new string[4];
            question.Answers[aIndex] = q1.TrueAnswer + ", " + q2.TrueAnswer;
            question.Answers[(aIndex + 1) % 4] = q1.FalseAnswer + ", " + q2.TrueAnswer;
            question.Answers[(aIndex + 2) % 4] = q1.FalseAnswer + ", " + q2.FalseAnswer;
            question.Answers[(aIndex + 3) % 4] = q1.TrueAnswer + ", " + q2.FalseAnswer;
            question.CorrectAnswer = aIndex;

            return question;
        }

